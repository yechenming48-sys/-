    function detectIntent(question) {
        const q = (question || '').trim();
        if (!q) return null;
        let best = null;
        let bestScore = 0;
        for (const rule of INTENT_RULES) {
            let score = 0;
            if (rule.re && rule.re.test(q)) score += 100;
            for (const w of rule.words) {
                if (q.includes(w)) score += Math.max(10, w.length * 3);
            }
            if (score > bestScore) {
                bestScore = score;
                best = rule;
            }
        }
        return bestScore > 0 ? best : null;
    }

    function intentBand(prob) {
        if (prob >= 70) return 'high';
        if (prob >= 50) return 'mid';
        return 'low';
    }

