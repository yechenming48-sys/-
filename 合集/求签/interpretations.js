  // 动态生成宫位释义函数
  // 结合了主星性格、宫位含义和特定解释
  getExplanation: function (palaceName, stars) {
    let result = "";
    
    // 获取宫位基本释义
    const pInfo = this.palaces[palaceName.replace(" (身宫)", "")];
    if (!pInfo) return "暂无此宫位解析。";

    result += `<div class="exp-section">
      <h3>【${palaceName}释义】</h3>
      <p>${pInfo.desc}</p>
      <p class="advice"><em>建议：${pInfo.advice}</em></p>
    </div>`;

    if (stars.length === 0) {
      result += `<div class="exp-section">
        <h3>【宫内无主星】</h3>
        <p>此宫位空无主星，代表该宫位受对宫（对角线位置的宫位）星曜影响极大。建议借用对宫的主星进行性格和运势推算，同时也代表该宫位对应的人生领域容易受到外界环境的牵引和变化。</p>
      </div>`;
      return result;
    }

    // 寻找主星并解析
    const majorStars = stars.filter(s => s.type === "major");
    if (majorStars.length > 0) {
      result += `<div class="exp-section">
        <h3>【主星坐守解析】</h3>`;
      
      majorStars.forEach(star => {
        const starName = star.rawName;
        const starInfo = this.stars[starName];
        
        if (starInfo) {
          // 查找是否有定制的星-宫解读
          const customExp = this.starInPalace[starName] && this.starInPalace[starName][palaceName.replace(" (身宫)", "")];
          
          result += `<div class="star-block">
            <h4>⭐ ${star.name}（五行：${starInfo.fiveElement}，属${starInfo.type}）</h4>
            <p><strong>星曜特质：</strong>${starInfo.character}</p>
            ${customExp ? `<p class="custom-exp"><strong>宫位解析：</strong>${customExp}</p>` : `
            <p><strong>通用能量：</strong>在${palaceName}代表该领域容易受到${starName}星特性的影响。体现为：追求${starInfo.strength}的正面能量，但需防范${starInfo.weakness}的负面阻碍。</p>`}
          </div>`;
        }
      });
      result += `</div>`;
    }

    // 寻找辅星与四化解析
    const otherStars = stars.filter(s => s.type !== "major");
    if (otherStars.length > 0) {
      result += `<div class="exp-section">
        <h3>【辅助星曜与四化影响】</h3>
        <ul>`;
      
      otherStars.forEach(star => {
        const sName = star.rawName;
        let expText = "";
        
        // 匹配吉凶星
        if (this.auxiliary[sName]) {
          expText = this.auxiliary[sName];
        }
        // 匹配四化
        if (star.sihua && this.sihua[star.sihua]) {
          expText += " " + this.sihua[star.sihua];
        }

        if (expText) {
          result += `<li><strong>${star.name}：</strong>${expText}</li>`;
        } else {
          result += `<li><strong>${star.name}：</strong>为宫位增添相应五行和能量波动。</li>`;
        }
      });

      result += `</ul></div>`;
    }

    return result;
  }
